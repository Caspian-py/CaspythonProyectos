"""
   Hipotenusa de un triángulo rectángulo**
   Fórmula: c² = a² + b²

"""

print("VAMOS A CALCULAR LA HIPOTENUSA DE UN TRIANGULO RECTANGULO.")
print("Necesito que me proporciones algunos datos, listo? bien.")

c1 = float(input("Cateto 1: "))
c2 = float(input("Cateto 2: "))
h = (c1**2 + c2**2) ** 0.5

print(f"La Hipotenusa del triangulo con Cateto 1: {c1} y Cateto 2: {c2} es igual a Hipotenusa = {h}")


